class Calculadora{
    sumar(num1,num2){
        return num1 + num2
    }

    restar(num1,num2){
        return num1 - num2
    }

    dividir(num1,num2){
        return num1/num2
        // return Number((num1 / num2).toFixed(7));
    }

    multiplicar(num1,num2){
        return num1 * num2
    }
}